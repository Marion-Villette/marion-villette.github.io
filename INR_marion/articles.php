    <h3>
      DROUIN, Anne-Marie.
    </h3>

    <h4>
      <a href="https://www.persee.fr/doc/aster_0297-9373_1987_num_4_1_916#aster_0297-9373_1987_num_4_1_T1_0001_0000" target="_blank">
        Des images et des sciences,
      </a>
    </h4> 
    
    <h5>
      Aster, 1987.
    </h5>
    
    <blockquote cit="https://www.persee.fr/doc/aster_0297-9373_1987_num_4_1_916#aster_0297-9373_1987_num_4_1_T1_0001_0000">Les images traduisent la réalité, mais traduire, c'est interpréter.</blockquote>
    
    
    <p> 
      Cet article questionne la place de l'image dans le domaine de la science. Il établit différents types d'images, l'illustration, la photographie et le schéma. Il propose d'explorer ces questions à la fois dans le contexte de l'image dans la science, mais aussi en considérant seulement l'image en elle-même. Il souhaite mettre en valeur les rôles parfois contradictoires mais essentiels que joue l'image dans la science, notamment dans sa transmission.
    </p>
   

