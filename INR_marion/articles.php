  <article class="filterDiv science design codesign">
    <h3>
      BOBROFF, Julien.  
    </h3>

    <h4>
      <a href="https://www.cairn.info/revue-communication-et-langages1-2015-1-page-9.htm" target="_blank">
      Objets de médiation de la science et objets de design, le cas du projet «&#8201Design quantique&#8201»,
      </a>
    </h4> 
    
    <h5>
      Presses universitaires de France, 2015.
    </h5>
    
    <blockquote cit="https://www.cairn.info/revue-communication-et-langages1-2015-1-page-9.htm">Le fait qu’il n’y a «&#8201rien à montrer&#8201» de façon directe offre [...] un espace de liberté créatrice pour le designer, 
      dont l’une des spécificités est de savoir mettre en forme et en matière un concept ou une idée abstraite.</blockquote>
    
    
    <p> 
      Cet article revient sur l'atelier Design Quantique, une collaboration entre le laboratoire de vulgarisation de la physique La physique autrement, et les étudiants de l'Ecole nationale supérieure de création industrielle de Paris en 2013. Pendant cet atelier, de nombreux objets de médiation ont été créés par les étudiants, et Julien Bobroff (cofondateur de La physique autrement) analyse ici avec plus de recul ces créations. Il répartit les objets en catégories en fonction des solutions trouvées&#8201: Les objets représentant les propriétés des objets quantiques, ceux qui établissent des analogies entre l'objet quantique et l'univers du quotidien, les dispositifs de médiation de la physique quantique, et enfin ceux qui créent une interprétation poétique et plastique de la physique quantique.
    </p>
   

    <h6> 
      lu en octobre 2022 
    </h6>
  
  </article>


  
