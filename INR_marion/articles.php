  <article class="filterDiv langage image">
    <h3>
      MARINO, Ingrid.  
    </h3>

    <h4>
      <a href="https://www.cairn.info/revue-communication-et-langages1-2015-1-page-9.htm" target="_blank">
      L'image mentale-médiateur de la communication verbale,
      </a>
    </h4> 
    
    <h5>
      Maison des Sciences de l'Homme, 1981.
    </h5>
    
    <blockquote cit="https://www.cairn.info/revue-communication-et-langages1-2015-1-page-9.htm">On ne peut pas visualiser l'abstrait directement, mais chaque concept abstrait évoque quelque chose du monde concret qui a été associé à sa formation.</blockquote>
    
    
    <p> 
      Cet article aborde les différentes fonctions de l'image mentale en tant que médiateur d'une connaissance abstraite, ici le langage, mais dont les idées peuvent également concerner la physique quantique. Par exemple, l'image mentale peut être redondante, à savoir simplement représenter l'énoncé verbal. Elle peut aussi être l'image visuelle d'une association avec le concret comme une figure rhétorique. Ingrid MARINO énonce également les difficultés que peuvent poser le décodage d'une image, et les différences entre les publics sachants et non-sachants.
    </p>
   

    <h6> 
      lu en octobre 2022 
    </h6>
  
  </article>

    
