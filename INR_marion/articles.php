  <article class="filterDiv mediation etudedecas science">
    <h3>
      BOBROFF, Julien.  
    </h3>

    <h4>
      <a href="https://hal.archives-ouvertes.fr/hal-02005694/document" target="_blank">
      Le design et l’art pour vulgariser la physique,
      </a>
    </h4> 
    
    <h5>
      Science and You, 2015.
    </h5>
    
    <blockquote cit="https://hal.archives-ouvertes.fr/hal-02005694/document">Les difficultés associées à la médiation de la physique fondamentale sont multiples&#8201: des phénomènes souvent invisibles, abstraits, non intuitifs, écrits dans une langue mathématique, et une certaine appréhension du public.</blockquote>
    
    
    <p> 
    La physique autrement est un laboratoire qui travaille en collaboration avec des designers afin de créer des projets de médiation de la physique, plus particulièrement la physique quantique. Cet article revient sur certains projets (ex: Physics Circus, un petit cirque en bois fonctionnant à l'aide de la supraconductivité), les analyse et questionne les réponses développées. 
    Cela permet au physicien d'expliquer la singularité de cette démarche de création et la diversité des projets produits avec lui.
    </p>
   

