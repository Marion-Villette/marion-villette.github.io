    <h3>
      RENON, Anne-Lyse.  
    </h3>

    <h4>
      <a href="https://tel.archives-ouvertes.fr/tel-01802325/document" target="_blank">
      Design et esthétique dans les pratiques de la science, 
      </a>
    </h4> 
    
    <h5>
      Thèse soutenue à l'EHESS Paris, le 29 mai 2018.
    </h5>
    
    <blockquote cit="https://tel.archives-ouvertes.fr/tel-01802325/document">D'un point de vue anthropologique, il nous semble qu'il y a l'ouverture d'un champ de réflexions sur les relations entre design et science, où entre épistémique et technique, le design qualifierait la relation esthétique inhérente à tout processus de recherche.</blockquote>
    
